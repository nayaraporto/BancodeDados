create database EmpresaBDexpress;
use EmpresaBDexpress;

create table DEPARTAMENTO (
    DEPCODIGO INT PRIMARY KEY,
    DEPNOME VARCHAR(50)
);

create table FUNCIONARIO (
    FUNCODIGO INT PRIMARY KEY,
    DEPCODIGO INT NOT NULL,
    FUNNOME VARCHAR(100),
    FUNNASCIMENTO DATE,
    FUNSALARIO NUMERIC(10,2),
    FOREIGN KEY (DEPCODIGO) REFERENCES DEPARTAMENTO(DEPCODIGO)
);

create table DEPENDENTE (
    DEPENCODIGO INT PRIMARY KEY,
    FUNCODIGO INT NOT NULL,
    DEPNOME VARCHAR(100),
    FOREIGN KEY (FUNCODIGO) REFERENCES FUNCIONARIO(FUNCODIGO)
);

